
public class DrawingsGrouped {

	public static void main(String[] args) {
		drawDot();
		drawDot();
		drawQuestionMark();
		drawDot();
		drawExclamationMark();
	}
	
	public static void drawExclamationMark()
	{
		System.out.println("!");
	}
	
	public static void drawQuestionMark()
	{
		System.out.println("?");
	}
	
	public static void drawDot()
	{
		System.out.println(".");
	}
	
}
